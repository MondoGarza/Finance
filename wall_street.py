        # type: (HandlerInput) -> Response
        intentConfirmation = handler_input.request_envelope.request.intent.confirmation_status

        slots = handler_input.request_envelope.request.intent.slots
        cash = slots["Cash"]
        stockSymb = slots["stockSymb"]
        stockPrice = slots["stockPrice"]
        # print(cash.value)
        # print(stockSymb.value)
        # print(stockPrice.value)
        if intentConfirmation.to_str() == 'CONFIRMED':
            print("Here Confirmed")
            speech_text = "Order Placed!"
        elif intentConfirmation.to_str() == 'DENIED':
            speech_text = "Order Cancelled!"
        elif intentConfirmation.to_str() == 'NONE':
            speech_text = "First Call"
        else:
            print("Here Blah")
            speech_text = 'Blah Blah Blah'

